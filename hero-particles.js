// Hero section — floating gold particle field using Three.js
// Runs only on pages that have .hero; degrades gracefully if Three unavailable.
(function () {
  'use strict';

  var hero = document.querySelector('.hero');
  if (!hero) return;

  // Inject canvas behind all hero children
  var canvas = document.createElement('canvas');
  canvas.id = 'sjc-hero-canvas';
  canvas.style.cssText = [
    'position:absolute',
    'inset:0',
    'width:100%',
    'height:100%',
    'pointer-events:none',
    'z-index:0',
    'opacity:0',
    'transition:opacity 1.2s ease',
  ].join(';');
  hero.insertBefore(canvas, hero.firstChild);

  // Lazy-load Three.js from CDN — non-blocking
  var script = document.createElement('script');
  script.src = 'https://unpkg.com/three@0.160.1/build/three.module.js';
  script.type = 'module';
  document.head.appendChild(script);

  // We use an inline module so we can import Three after it loads
  var init = document.createElement('script');
  init.type = 'module';
  init.textContent = [
    "import * as THREE from 'https://unpkg.com/three@0.160.1/build/three.module.js';",
    "(function(){",
    "  var canvas = document.getElementById('sjc-hero-canvas');",
    "  if(!canvas) return;",
    "  var hero = canvas.parentElement;",
    "",
    "  var W = hero.offsetWidth, H = hero.offsetHeight;",
    "  var renderer = new THREE.WebGLRenderer({ canvas: canvas, antialias: false, alpha: true });",
    "  renderer.setPixelRatio(Math.min(window.devicePixelRatio, 1.5));",
    "  renderer.setSize(W, H);",
    "  renderer.setClearColor(0x000000, 0);",
    "",
    "  var scene  = new THREE.Scene();",
    "  var camera = new THREE.PerspectiveCamera(60, W / H, 0.1, 200);",
    "  camera.position.z = 40;",
    "",
    "  // ── Particle geometry ──────────────────────────────────────────────",
    "  var COUNT = 320;",
    "  var geo   = new THREE.BufferGeometry();",
    "  var pos   = new Float32Array(COUNT * 3);",
    "  var vel   = new Float32Array(COUNT * 3); // drift velocity per particle",
    "  var sizes = new Float32Array(COUNT);",
    "  var alphas= new Float32Array(COUNT);",
    "",
    "  for (var i = 0; i < COUNT; i++) {",
    "    pos[i*3]   = (Math.random() - 0.5) * 80;",
    "    pos[i*3+1] = (Math.random() - 0.5) * 50;",
    "    pos[i*3+2] = (Math.random() - 0.5) * 30;",
    "    vel[i*3]   = (Math.random() - 0.5) * 0.008;",
    "    vel[i*3+1] = 0.012 + Math.random() * 0.018;  // rise upward",
    "    vel[i*3+2] = (Math.random() - 0.5) * 0.005;",
    "    sizes[i]   = 0.5 + Math.random() * 2.5;",
    "    alphas[i]  = 0.2 + Math.random() * 0.6;",
    "  }",
    "",
    "  geo.setAttribute('position', new THREE.BufferAttribute(pos,  3));",
    "  geo.setAttribute('aSize',    new THREE.BufferAttribute(sizes,1));",
    "  geo.setAttribute('aAlpha',   new THREE.BufferAttribute(alphas,1));",
    "",
    "  // ── Shader material — round soft discs ─────────────────────────────",
    "  var mat = new THREE.ShaderMaterial({",
    "    transparent: true,",
    "    depthWrite:  false,",
    "    blending:    THREE.AdditiveBlending,",
    "    uniforms:    { uTime: { value: 0 } },",
    "    vertexShader: [",
    "      'attribute float aSize;',",
    "      'attribute float aAlpha;',",
    "      'varying   float vAlpha;',",
    "      'void main(){',",
    "      '  vAlpha = aAlpha;',",
    "      '  vec4 mv = modelViewMatrix * vec4(position,1.0);',",
    "      '  gl_Position  = projectionMatrix * mv;',",
    "      '  gl_PointSize = aSize * (300.0 / -mv.z);',",
    "      '}',",
    "    ].join('\\n'),",
    "    fragmentShader: [",
    "      'varying float vAlpha;',",
    "      'void main(){',",
    "      '  float d = length(gl_PointCoord - vec2(0.5));',",
    "      '  if(d > 0.5) discard;',",
    "      '  float a = smoothstep(0.5, 0.1, d) * vAlpha;',",
    "      '  gl_FragColor = vec4(0.98, 0.84, 0.38, a);',  // gold",
    "      '}',",
    "    ].join('\\n'),",
    "  });",
    "",
    "  var points = new THREE.Points(geo, mat);",
    "  scene.add(points);",
    "",
    "  // ── Resize handling ────────────────────────────────────────────────",
    "  function onResize(){",
    "    W = hero.offsetWidth; H = hero.offsetHeight;",
    "    camera.aspect = W / H;",
    "    camera.updateProjectionMatrix();",
    "    renderer.setSize(W, H);",
    "  }",
    "  window.addEventListener('resize', onResize, { passive: true });",
    "",
    "  // ── RAF loop ───────────────────────────────────────────────────────",
    "  var clock = new THREE.Clock();",
    "  var posAttr = geo.attributes.position;",
    "  var yMax = 26, yMin = -26;",
    "",
    "  function tick(){",
    "    var dt = clock.getDelta();",
    "    mat.uniforms.uTime.value += dt;",
    "",
    "    for(var i = 0; i < COUNT; i++){",
    "      posAttr.array[i*3]   += vel[i*3];",
    "      posAttr.array[i*3+1] += vel[i*3+1];",
    "      posAttr.array[i*3+2] += vel[i*3+2];",
    "      // wrap: when particle floats above top, teleport to bottom",
    "      if(posAttr.array[i*3+1] > yMax){",
    "        posAttr.array[i*3+1] = yMin;",
    "        posAttr.array[i*3]   = (Math.random()-0.5)*80;",
    "      }",
    "    }",
    "    posAttr.needsUpdate = true;",
    "",
    "    // Gentle parallax with mouse",
    "    if(window._sjcMX !== undefined){",
    "      points.rotation.y += (window._sjcMX * 0.04 - points.rotation.y) * 0.02;",
    "      points.rotation.x += (window._sjcMY * 0.02 - points.rotation.x) * 0.02;",
    "    }",
    "",
    "    renderer.render(scene, camera);",
    "    requestAnimationFrame(tick);",
    "  }",
    "  tick();",
    "",
    "  // Fade canvas in after first frame",
    "  setTimeout(function(){ canvas.style.opacity = '1'; }, 80);",
    "",
    "  // Mouse parallax",
    "  window._sjcMX = 0; window._sjcMY = 0;",
    "  window.addEventListener('mousemove', function(e){",
    "    window._sjcMX = (e.clientX / window.innerWidth  - 0.5) * 2;",
    "    window._sjcMY = (e.clientY / window.innerHeight - 0.5) * 2;",
    "  }, { passive: true });",
    "})();",
  ].join('\n');
  document.head.appendChild(init);
})();
